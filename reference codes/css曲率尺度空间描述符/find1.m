%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%%%%%%返回匹配的点数的下标
%%%%%%%%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
function Uij=find1(Pij) 
[i,j]=size(Pij);
for ii=1:i
    [a,b]=max(Pij(ii,:));
    [c,d]=max(Pij(:,b));
    if a==c
        Uij(ii,:)=[d b];
    else
        Uij(ii,:)=[0 0];
    end
end
